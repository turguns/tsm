<title>The Talking Kyrgyz Phrasebook</title>

<p><b>Kyrgyz</b> is a language spoken by 6 million people primarily in <a href="/en/Kyrgyzstan" title="Kyrgyzstan">Kyrgyzstan</a> and, to a lesser extent, in adjacent regions such as <a href="/en/Kazakhstan" title="Kazakhstan">Kazakhstan</a>, <a href="/en/Xinjiang" title="Xinjiang">Xinjiang</a> (<a href="/en/China" title="China">China</a>), <a href="/en/Uzbekistan" title="Uzbekistan">Uzbekistan</a>, &amp; <a href="/en/Tajikistan" title="Tajikistan">Tajikistan</a>. In Kyrgyzstan, the language is co-official with Russian, which is the predominate language used in urban areas (especially <a href="/en/Bishkek" title="Bishkek">Bishkek</a>) while Kyrgyz is predominate in rural areas and small cities/towns. It is a Turkic language and has many similarities with languages such as <a href="/en/Turkish_phrasebook" title="Turkish phrasebook">Turkish</a>, <a href="/wiki/en/index.php?title=Tartar_phrasebook&amp;action=edit&amp;redlink=1" class="new" title="Tartar phrasebook (page does not exist)">Tartar</a>, <a href="/en/Kazakh_phrasebook" title="Kazakh phrasebook">Kazakh</a>, <a href="/en/Azeri_phrasebook" class="mw-redirect" title="Azeri phrasebook">Azeri</a>, &amp; <a href="/en/Uzbek_phrasebook" title="Uzbek phrasebook">Uzbek</a>. As a result of close ties culturally and economically, Kyrgyz has become increasingly mutually intelligible with <a href="/en/Kazakh_phrasebook" title="Kazakh phrasebook">Kazakh</a> in recent decades. 
</p>
<h2><span class="mw-headline" id="Script">Script</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/wiki/en/index.php?title=Kyrgyz_phrasebook&amp;action=edit&amp;section=1" title="Edit section: Script">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The alphabet used for Kyrgyz was originally an Arabic script followed by a 20th century conversion to Roman script, much like the Arabic-Roman switch in other Turkic languages. However, Cyrillic script was forced on the language by the Soviet government (as they did with all other Turkic languages) and Cyrillic remains the alphabet used today by the overwhelming percentage of Kyrgyz speakers except in China, where Arabic script is the official script imposed by Chinese authorities.
</p>


